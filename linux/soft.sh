#!/usr/bin/env bash

# 我们日常使用电脑，假设我们只用打开一个编辑软件，我们只要熟悉几个命令就好了。

# 1 . 开机
# 按电脑开机按钮。

# 2. 登录
# 要求我们输入账号密码，或我们手动登录。
sudo login USER
# 或者 
su - USER

# 3. 打开编辑器 vim 写文字
vim

# 4.关闭计算机
sudo shutdown -h now

# -------------------------------------
# 第三步还可能 做其他事情
# 显示文章目录
ls
# 显示打开程序
ps




